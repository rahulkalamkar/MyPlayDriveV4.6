package com.hungama.myplay.activity.data.dao.campaigns;

import java.io.Serializable;

/**
 * Enumeration definition of different placement types.
 */
public enum PlacementType implements Serializable {
	MUSIC_NEW, MUSIC_POPULAR, MUSIC_RECOMMENDED,
	// PLAYLIST_AD_UNIT, // v4.4
	MUSIC_NEW_PLAYLIST, // v4.4
	MUSIC_POP_PLAYLIST, // v4.4
	// VIDEOS_NEW,
	VIDEO_NEW, // v4.4
	VIDEOS_POPULAR, VIDEOS_RECOMMENDED, SEARCH_TAG, SEARCH_RESULTS, DISCOVERY, DISCOVERY_LISTING, DISCOVERY_MOOD_SELECTOR, DISCOVERY_CATEGORY_SELECTOR, DISCOVERY_ERA_SELECTOR, DISCOVERY_TEMPO_SELECTOR, SPLASH_SCREEN, SPLASH_ANIM, SONG_DETAILS_LEFT, SONG_DETAILS_RIGHT, PLAYLIST_DETAILS, ALBUM_DETAILS, PLAYER_INFO,
	// PLAYER_SIMILAR,
	// PLAYER_ALBUM,
	PLAYER_LYRICS, PLAYER_TRIVIA,
	// PLAYER_VIDEOS,
	// PLAYER_ALBUM_ART_OVERLAY,
	PLAYER_ALBUM_ART_OVERLAY_FULL, // v4.4
	DISCOVERY_PLAYER_ARTWORK, // v4.4
	RADIO_PLAYER_ARTWORK, // v4.4
	PLAYER_ALBUM_BANNER, // v4.4
	PLAYER_VIDEOS_BANNER, // v4.4
	PLAYER_SIMILAR_BANNER, // v4.4
	VIDEO_RELATED_BANNER, // v4.4
	// LIVE_RADIO,
	// CELEB_RADIO,
	LIVE_RADIO_BANNER,
	// CELEB_RADIO_BANNER,
	DEMAND_RADIO_BANNER, RADIO_CHANNEL, VIDEO_PORTRAIT_BANNER, VIDEO_LANDSCAPE_OVERLAY, VIDEO_INFO,
	// VIDEO_RELATED,
	AUDIO_AD, VIDEO_AD;

	public static boolean contains(String test) {

		for (PlacementType c : PlacementType.values()) {
			if (c.toString().equals(test)) {
				return true;
			}
		}

		return false;
	}

}
